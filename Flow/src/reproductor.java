    private void reproducirCancion() throws BasicPlayerException {
        // Ruta de la canción que deseas reproducir
       if (!canciones.isEmpty()) {
        String rutaCancion = canciones.get(0); // Obtener la primera canción de la lista
        try {
            correr.open(new File(rutaCancion)); // Abre el archivo de música
            correr.play(); // Inicia la reproducción
        } catch (BasicPlayerException e) {
            e.printStackTrace();
        }
    }
    }


   private void detenerReproduccion() {
    try {
        correr.stop(); // Detiene la reproducción
    } catch (BasicPlayerException e) {
        e.printStackTrace();
    }
}

