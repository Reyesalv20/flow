    private void addMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addMouseClicked
        //AGREGAR CANCION

        JFileChooser filechooser = new JFileChooser();
        filechooser.setFileFilter(new FileNameExtensionFilter("Archivo MP3", "mp3", "mp3"));
        filechooser.setFileSelectionMode(JFileChooser.FILES_ONLY);
        filechooser.setMultiSelectionEnabled(true);
        int seleccion = filechooser.showOpenDialog(this);
         
       
        if (seleccion == JFileChooser.APPROVE_OPTION) {
            File files[] = filechooser.getSelectedFiles();
            boolean noMp3 = false;
            
            File selectedFile = filechooser.getSelectedFile();
            String rutaCancion = selectedFile.getAbsolutePath();
            canciones.add(rutaCancion);
            String name=selectedFile.getName();
            int cont=1;
            list=cont+"."+list+"\n"+name;
            jTextArea1.setText(list);
            nom_can.setText(name);
          cont++;
            
        }
        


    }//GEN-LAST:event_addMouseClicked

